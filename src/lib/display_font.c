#define FONT_BMP(ch, d0,d1,d2,d3,d4,d5,d6,d7,d8,d9,d10,d11,d12,d13,d14,d15) \
    memcpy(font[(ch)].bitmap, \
            (uint8_t[]){d0,d1,d2,d3,d4,d5,d6,d7,d8,d9,d10,d11,d12,d13,d14,d15}, \
            16)

static FontLetter font[256];

void display_char(FontSettings* fs, const uint8_t ch, uint32_t r, uint32_t c) {
    FontLetter fl = font[ch];
    uint32_t scale = fs->scale;
    uint32_t width = fs->width;
    uint32_t height = fs->height;
    uint32_t color = fs->color;

    for (uint32_t i = 0; i < FONT_HEIGHT; i++) {
        for (uint32_t j = 0; j < FONT_WIDTH; j++) {
            if (!((fl.bitmap[i] >> (FONT_WIDTH - j - 1)) & 1)) {
                continue;
            }

            for (uint32_t ii = 0; ii < scale; ii++) {
                uint32_t row = r + i * scale + ii;
                if (row >= height) {
                    break;
                }

                for (uint32_t jj = 0; jj < scale; jj++) {
                    uint32_t col = c + j * scale + jj;
                    if (col >= width) {
                        break;
                    }

                    fs->fb[row * width + col] = color;
                }
            }
        }
    }
}
void display_text(FontSettings* fs, const uint8_t* s, uint32_t n,
        uint32_t r, uint32_t c) {
    for (uint32_t i = 0; i < n; i++) {
        display_char(fs, s[i], r, c + FONT_WIDTH * fs->scale * i);
    }
}

